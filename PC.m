clc;
clear all;
close all;
f0 = 10e9;
B = 10e6;
Tp = 10e-6;
fs = 100e6;
R = 3000;
c = 3e8;
N = 4096;
b = B/Tp;
tt = 2*R/c;
t = (0:N)/fs;
s0 = rectpuls(t,Tp).*exp(1i*pi*b*(t).^2);
sb = rectpuls(t-tt,Tp).*exp(1i*pi*b*(t-tt).^2)*exp(-2i*pi*f0*tt);
S0 = fft(s0);
SB = fft(sb);
SS = ifft(S0.*conj(SB));
figure(1);
plot(t,real(sb));
figure(2);
plot(t,abs(SS));